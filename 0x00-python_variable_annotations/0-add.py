#!/usr/bin/env python3
"""
Module with 'add' function. It takes two floats and returns their sum.
"""


def add(a: float, b: float) -> float:
    """
    Function to add two floats.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: Sum of a and b.
    """
    return a + b
