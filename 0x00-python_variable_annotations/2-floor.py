#!/usr/bin/env python3
"""
Module with a function 'floor' that computes the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Computes the floor of a float.

    Args:
        n (float): Input float number.

    Returns:
        int: Floor of the input float.
    """
    return math.floor(n)
